# a function that adds two numbers and returns the result

def add(num1, num2):
    return num1 + num2

# a function that subtracts one number from another and returns the result

def subtract(num1, num2):
    return num1 - num2

# a function that multiplies two numbers and returns the result

def multiply(num1, num2):
    return num1 * num2

# a function that divides one number by another and returns the result as a floating-point number

def divide(num1, num2):
    return num1 / num2

# a function that divides one number by another and returns the result as a whole number (integer division)

def integer_divide(num1, num2):
    return num1 // num2

# a function that divides one number by another and returns the remainder (modulo operation)

def modulo(num1, num2):
    return num1 % num2

# a function that raises one number to the power of another and returns the result

def exponeniate(num1, num2):
    return num1 ** num2